export default class Calculator {
    sum(x, y) { return x + y }

    subtract(x, y) { return x - y }

    multiply(x, y) { return x * y }

    divide(x, y) { return x / y }

    power(base, exponent) { return base ** exponent }

    square(x) { return x * x };

    percent(x) { return x / 100 }

    invert(x) { return (x) * (-1) }
}